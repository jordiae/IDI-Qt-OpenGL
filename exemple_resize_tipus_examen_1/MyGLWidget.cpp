double MyGLWidget::distancia_punts(glm::vec3 a, glm::vec3 b) {
  return sqrt((b.x-a.x)*(b.x-a.x)+(b.y-a.y)*(b.y-a.y)+(b.z-a.z)*(b.z-a.z));
}


